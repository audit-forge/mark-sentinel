"""
M.A.R.K. Sentinel — Kyverno ClusterPolicy generator
Produces Kyverno admission control policies that enforce M.A.R.K. controls
on Kubernetes AI workloads.

Apply: kubectl apply -f mark_sentinel_kyverno.yaml
Target: Deployments/Pods/StatefulSets with label mark-sentinel.io/ai-workload: "true"

Policy index:
  mark-sentinel-ai-deploy-001  No plaintext credentials in env vars  (AI-DEPLOY-001)
  mark-sentinel-ai-deploy-005  Require TLS on AI Ingress resources   (AI-DEPLOY-005)
  mark-sentinel-ai-deploy-006  Require CPU/memory resource limits    (AI-DEPLOY-006)
  mark-sentinel-ai-agent-001   Require non-root securityContext      (AI-AGENT-001)
  mark-sentinel-ai-deploy-004  Disallow hostNetwork on AI workloads  (AI-DEPLOY-004)
  mark-sentinel-ai-supply-005  Require pinned image tag              (AI-SUPPLY-005)
"""
from datetime import date


_AI_LABEL = 'mark-sentinel.io/ai-workload: "true"'

_WORKLOAD_KINDS = ["Deployment", "StatefulSet", "Pod"]


def _meta(name: str, check_id: str, title: str, action: str, today: str) -> str:
    return f"""\
apiVersion: kyverno.io/v1
kind: ClusterPolicy
metadata:
  name: {name}
  annotations:
    mark-sentinel.io/check-id: "{check_id}"
    mark-sentinel.io/title: "{title}"
    mark-sentinel.io/generated: "{today}"
    policies.kyverno.io/title: "M.A.R.K. Sentinel — {check_id}"
    policies.kyverno.io/description: >-
      Enforces M.A.R.K. Sentinel control {check_id}: {title}.
      Generated by M.A.R.K. Sentinel AI Security Audit Tool.
spec:
  validationFailureAction: {action}
  background: true"""


def _policy_no_plaintext_creds(today: str, action: str) -> str:
    header = _meta(
        "mark-sentinel-ai-deploy-001",
        "AI-DEPLOY-001",
        "API Keys Not Exposed in Env Vars",
        action,
        today,
    )
    return header + """
  rules:
    - name: disallow-plaintext-ai-credentials
      match:
        any:
        - resources:
            kinds:
            - Deployment
            - StatefulSet
            - Pod
            selector:
              matchLabels:
                mark-sentinel.io/ai-workload: "true"
      validate:
        message: >-
          [AI-DEPLOY-001] AI workload containers must not set API keys, tokens, or
          passwords as plaintext env var values. Use secretKeyRef instead.
        foreach:
          - list: >-
              request.object.spec.template.spec.containers[] |
              map(&env[] || `[]`, @) | [] | []
            deny:
              conditions:
                all:
                - key: "{{ element.value || '' }}"
                  operator: NotEquals
                  value: ""
                - key: "{{ element.name }}"
                  operator: Match
                  value: "(?i).*(API_KEY|SECRET|TOKEN|PASSWORD|OPENAI|ANTHROPIC|GEMINI|GROQ).*"
"""


