# Map check_id to policy generator so we can selectively include based on findings
_POLICY_MAP = {
    "AI-DEPLOY-001": _policy_no_plaintext_creds,
    "AI-DEPLOY-004": _policy_no_host_network,
    "AI-DEPLOY-005": _policy_require_tls_ingress,
    "AI-DEPLOY-006": _policy_require_resource_limits,
    "AI-AGENT-001": _policy_require_non_root,
    "AI-SUPPLY-005": _policy_require_pinned_image,
}


def format_kyverno(results: list, profile: dict, target: str, mode: str) -> str:
    today = str(date.today())
    profile_name = profile.get("name", "default")

    # Use Enforce for checks that failed/warned, Audit for everything else
    failed_ids = {r.check_id for r in results if r.status in ("FAIL", "WARN")}

    header_comment = f"""\
# M.A.R.K. Sentinel — Kyverno ClusterPolicy Bundle
# Profile  : {profile_name}
# Target   : {target}
# Generated: {today}
#
# Apply:   kubectl apply -f mark_sentinel_kyverno.yaml
# Remove:  kubectl delete -f mark_sentinel_kyverno.yaml
#
# These policies enforce M.A.R.K. Sentinel AI security controls on Kubernetes
# workloads. To target a workload, add the label:
#   {_AI_LABEL}
#
# Policies from this scan:
#   validationFailureAction: Enforce  — check FAILED or WARNED in this scan
#   validationFailureAction: Audit    — check passed (monitor only)
#
# To exempt a specific workload from a policy, add:
#   kyverno.io/exempt: "true"  label to the resource.

"""

    docs = [header_comment]
    for check_id, generator in _POLICY_MAP.items():
        action = "Enforce" if check_id in failed_ids else "Audit"
        docs.append(generator(today, action))

    return "\n---\n".join(docs)
